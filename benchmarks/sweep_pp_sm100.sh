#!/usr/bin/env bash
# B200 / sm_100 profile for the canonical pp sweep.
#
# This wrapper keeps the benchmark flow, Python baselines, and aggregation in
# sweep_pp.sh. Only sm_100-specific Grout tuning knobs live here so the profile
# is easy to inspect and revise.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

: "${GROUT_TUNING_PROFILE:=sm100_b200}"

# Keep TRT-LLM out of these local paper sweeps unless explicitly requested.
: "${SWEEP_ENABLE_TRTLLM:=0}"

# Long-context pp cells dominate runtime; keep short cells at BENCH_REPS and
# run pp > 512 with fewer measured reps unless explicitly overridden.
: "${BENCH_REPS_LONG:=3}"

# Decode path profile selected from B200 32B tg512/tg36 smoke tests.
: "${GROUT_CUDA_GRAPH_DECODE:=1}"
: "${GROUT_FLASH_DECODE:=0}"
: "${GROUT_FMHA_SPLIT_KV:=1}"
: "${GROUT_FMHA_DECODE_LATENCY:=4}"
: "${GROUT_FMHA_DECODE_OCCUPANCY:=2}"
: "${GROUT_FMHA_MERGE_CHUNK_D:=16}"
: "${GROUT_FMHA_MERGE_LATENCY:=2}"
: "${GROUT_FUSED_QK_ROPE_KV_DECODE:=1}"
: "${GROUT_QK_ROPE_LATENCY:=2}"
: "${GROUT_QK_ROPE_OCCUPANCY:=1}"
: "${GROUT_QK_ROPE_CGA:=0}"
: "${GROUT_KV_CACHE_DYN_CHUNK_D:=32}"
: "${GROUT_EMBED_BLOCK:=1024}"
: "${GROUT_RMS_BLOCK:=8192}"
: "${GROUT_ARGMAX_BLOCK:=128}"

# Prefill support kernels. KV_CACHE_BM_S=16 is retained from the 5090 pp2048
# work; B200 sweeps so far did not beat it.
: "${GROUT_KV_CACHE_BM_S:=16}"
: "${GROUT_FUSED_QK_ROPE_KV_PREFILL:=1}"

# Per-pp attention overrides. Unspecified pp cells use sweep_pp.sh's existing
# 5090 defaults. BM=16, BN=64, GROUP=model query-group-size, SWIZZLE=8,
# SCHED=1, MASK_SPLIT=0, OCC=2, LAT=4 is the tuned B200 Qwen3-32B Tile IR LPT
# shape after the f16 P x V MMA fix.
: "${GROUT_ATTN_BM_PREFILL_PP_2048:=16}"
: "${GROUT_ATTN_BN_PREFILL_PP_2048:=64}"
: "${GROUT_FMHA_PREFILL_GQA_PP_2048:=0}"
: "${GROUT_FMHA_PREFILL_GQA_LPT_PP_2048:=1}"
: "${GROUT_FMHA_PREFILL_GQA_GROUP_PP_2048:=0}"
: "${GROUT_FMHA_PREFILL_LPT_SWIZZLE_PP_2048:=8}"
: "${GROUT_FMHA_PREFILL_LPT_SCHED_PP_2048:=1}"
: "${GROUT_FMHA_PREFILL_LPT_MASK_SPLIT_PP_2048:=0}"
: "${GROUT_FMHA_PREFILL_LATENCY_PP_2048:=4}"
: "${GROUT_FMHA_PREFILL_OCCUPANCY_PP_2048:=2}"

# Long-context pp baseline: start from the tuned pp=2048 Tile IR LPT profile.
: "${GROUT_ATTN_BM_PREFILL_PP_8192:=$GROUT_ATTN_BM_PREFILL_PP_2048}"
: "${GROUT_ATTN_BN_PREFILL_PP_8192:=$GROUT_ATTN_BN_PREFILL_PP_2048}"
: "${GROUT_FMHA_PREFILL_GQA_PP_8192:=$GROUT_FMHA_PREFILL_GQA_PP_2048}"
: "${GROUT_FMHA_PREFILL_GQA_LPT_PP_8192:=$GROUT_FMHA_PREFILL_GQA_LPT_PP_2048}"
: "${GROUT_FMHA_PREFILL_GQA_GROUP_PP_8192:=$GROUT_FMHA_PREFILL_GQA_GROUP_PP_2048}"
: "${GROUT_FMHA_PREFILL_LPT_SWIZZLE_PP_8192:=$GROUT_FMHA_PREFILL_LPT_SWIZZLE_PP_2048}"
: "${GROUT_FMHA_PREFILL_LPT_SCHED_PP_8192:=$GROUT_FMHA_PREFILL_LPT_SCHED_PP_2048}"
: "${GROUT_FMHA_PREFILL_LPT_MASK_SPLIT_PP_8192:=$GROUT_FMHA_PREFILL_LPT_MASK_SPLIT_PP_2048}"
: "${GROUT_FMHA_PREFILL_LATENCY_PP_8192:=$GROUT_FMHA_PREFILL_LATENCY_PP_2048}"
: "${GROUT_FMHA_PREFILL_OCCUPANCY_PP_8192:=$GROUT_FMHA_PREFILL_OCCUPANCY_PP_2048}"
: "${GROUT_ATTN_BN_DECODE_PP_8192:=64}"
: "${GROUT_FMHA_NUM_KV_SPLITS_PP_8192:=32}"

export \
    GROUT_TUNING_PROFILE \
    SWEEP_ENABLE_TRTLLM \
    BENCH_REPS_LONG \
    GROUT_CUDA_GRAPH_DECODE \
    GROUT_FLASH_DECODE \
    GROUT_FMHA_SPLIT_KV \
    GROUT_FMHA_DECODE_LATENCY \
    GROUT_FMHA_DECODE_OCCUPANCY \
    GROUT_FMHA_MERGE_CHUNK_D \
    GROUT_FMHA_MERGE_LATENCY \
    GROUT_FUSED_QK_ROPE_KV_DECODE \
    GROUT_QK_ROPE_LATENCY \
    GROUT_QK_ROPE_OCCUPANCY \
    GROUT_QK_ROPE_CGA \
    GROUT_KV_CACHE_DYN_CHUNK_D \
    GROUT_EMBED_BLOCK \
    GROUT_RMS_BLOCK \
    GROUT_ARGMAX_BLOCK \
    GROUT_KV_CACHE_BM_S \
    GROUT_FUSED_QK_ROPE_KV_PREFILL \
    GROUT_ATTN_BM_PREFILL_PP_2048 \
    GROUT_ATTN_BN_PREFILL_PP_2048 \
    GROUT_FMHA_PREFILL_GQA_PP_2048 \
    GROUT_FMHA_PREFILL_GQA_LPT_PP_2048 \
    GROUT_FMHA_PREFILL_GQA_GROUP_PP_2048 \
    GROUT_FMHA_PREFILL_LPT_SWIZZLE_PP_2048 \
    GROUT_FMHA_PREFILL_LPT_SCHED_PP_2048 \
    GROUT_FMHA_PREFILL_LPT_MASK_SPLIT_PP_2048 \
    GROUT_FMHA_PREFILL_LATENCY_PP_2048 \
    GROUT_FMHA_PREFILL_OCCUPANCY_PP_2048 \
    GROUT_ATTN_BM_PREFILL_PP_8192 \
    GROUT_ATTN_BN_PREFILL_PP_8192 \
    GROUT_FMHA_PREFILL_GQA_PP_8192 \
    GROUT_FMHA_PREFILL_GQA_LPT_PP_8192 \
    GROUT_FMHA_PREFILL_GQA_GROUP_PP_8192 \
    GROUT_FMHA_PREFILL_LPT_SWIZZLE_PP_8192 \
    GROUT_FMHA_PREFILL_LPT_SCHED_PP_8192 \
    GROUT_FMHA_PREFILL_LPT_MASK_SPLIT_PP_8192 \
    GROUT_FMHA_PREFILL_LATENCY_PP_8192 \
    GROUT_FMHA_PREFILL_OCCUPANCY_PP_8192 \
    GROUT_ATTN_BN_DECODE_PP_8192 \
    GROUT_FMHA_NUM_KV_SPLITS_PP_8192

exec bash "$SCRIPT_DIR/sweep_pp.sh" "$@"
