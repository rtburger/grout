: "${GROUT_FMHA_PREFILL_OCCUPANCY_PP_16384:=$GROUT_FMHA_PREFILL_OCCUPANCY_PP_8192}"
: "${GROUT_ATTN_BN_DECODE_PP_16384:=$GROUT_ATTN_BN_DECODE_PP_8192}"
: "${GROUT_FMHA_NUM_KV_SPLITS_PP_16384:=$GROUT_FMHA_NUM_KV_SPLITS_PP_8192}"

: "${GROUT_ATTN_BM_PREFILL_PP_32768:=$GROUT_ATTN_BM_PREFILL_PP_8192}"
: "${GROUT_ATTN_BN_PREFILL_PP_32768:=$GROUT_ATTN_BN_PREFILL_PP_8192}"
: "${GROUT_FMHA_PREFILL_GQA_PP_32768:=$GROUT_FMHA_PREFILL_GQA_PP_8192}"
: "${GROUT_FMHA_PREFILL_GQA_LPT_PP_32768:=$GROUT_FMHA_PREFILL_GQA_LPT_PP_8192}"
