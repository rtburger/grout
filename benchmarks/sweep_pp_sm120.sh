# run pp > 512 with fewer measured reps unless explicitly overridden.
: "${BENCH_REPS_LONG:=3}"

# Decode path profile selected from the 5090 decode sweeps.
: "${GROUT_CUBLAS_COMPUTE16:=1}"
: "${GROUT_CUDA_GRAPH_DECODE:=1}"
: "${GROUT_FLASH_DECODE:=0}"
: "${GROUT_FMHA_SPLIT_KV:=1}"
: "${GROUT_FMHA_DECODE_LATENCY:=4}"
